    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // Biến Search
        String textSearch = txtSearch.getText().trim().toLowerCase();
        if (textSearch.isEmpty()) {
            ResultSet rs = UserBLL.LayTatCaUser();
            UserBLL.DoDuLieuvaoJtaBle(rs, tblUser);
        } else {
            switch (cbxOptionSearch.getSelectedItem().toString()) {
                case "Search All": {
                    ResultSet rs = UserBLL.LayTatCaUser();
                    UserBLL.DoDuLieuvaoJtaBle(rs, tblUser);
                    UserBLL.TimKiemTatCa(textSearch, tblUser);
                    break;
                }
                case "User Name": {
                    ResultSet rs = UserBLL.LayTatCaUser();
                    UserBLL.DoDuLieuvaoJtaBle(rs, tblUser);
                    UserBLL.timKiemUser(textSearch, tblUser);
                    break;
                }
                case "User Type":{
                    ResultSet rs = UserBLL.LayTatCaUser();
                    UserBLL.DoDuLieuvaoJtaBle(rs, tblUser);
                    UserBLL.timKiemVaiTro(textSearch, tblUser);
                    break;
                }
            }
            ResultSet rs = UserBLL.LayTatCaUser();
            UserBLL.DoDuLieuvaoJtaBle(rs, tblUser);
            UserBLL.TimKiemTatCa(textSearch, tblUser);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

