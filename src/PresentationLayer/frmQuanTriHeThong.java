    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        // TODO add your handling code here:
        int row = tblUser.getSelectedRow();
        if (row != -1) {
            txtTenUser.setText(tblUser.getValueAt(row, 1).toString());
            txtUsername.setText(tblUser.getValueAt(row, 2).toString());
            txtPassword.setText(tblUser.getValueAt(row, 3).toString());
            String vaiTro = tblUser.getValueAt(row, 4).toString();
            if (vaiTro.contains("Quản Trị")) {
                chbQuanTri.setSelected(true);
            } else {
                chbQuanTri.setSelected(false);
            }
            if (vaiTro.contains("Sản Xuất")) {
                chbSanXuat.setSelected(true);
            } else {
                chbSanXuat.setSelected(false);
            }

            if (vaiTro.contains("Kế Hoạch")) {
                chbKeHoach.setSelected(true);
            } else {
                chbKeHoach.setSelected(false);
            }

        }
    }//GEN-LAST:event_tblUserMouseClicked

