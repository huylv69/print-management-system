    private void tblMayInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMayInMouseClicked
        congThucTienIn = new CongThuc();
        congThucTienTho = new CongThuc();
        tblCongThucThanhPhanTienIn = new JTable();
        tblCongThucThanhPhanTienTho = new JTable();
        int row = tblMayIn.getSelectedRow();
        if (row != -1) {
            txtMaMayIn.setText(tblMayIn.getValueAt(row, 1).toString());
            txtTenMayIn.setText(tblMayIn.getValueAt(row, 2).toString());
            txtCtTienIn.setText(tblMayIn.getValueAt(row, 3).toString());
            txtCtTienTho.setText(tblMayIn.getValueAt(row, 4).toString());
        }
        String maMayIn = txtMaMayIn.getText();
        try {
            ResultSet rsCongThucTienIn = MayInDAL.layCongThucIn(maMayIn, true);
            if (rsCongThucTienIn.next()) {
                congThucTienIn = new CongThuc(rsCongThucTienIn.getInt("ma_ct"), rsCongThucTienIn.getString("ten_ct"), rsCongThucTienIn.getString("loai"), rsCongThucTienIn.getString("ma_mayin"));
                //Công thức đã lưu
                ResultSet resultSet = MayInDAL.layCongThuc(rsCongThucTienIn.getInt("ma_ct"));
                frmCongThuc ct = new frmCongThuc();
                DefaultTableModel tblModelTienIn = (DefaultTableModel) ct.tblCongThucThanhPhan.getModel();
                while (resultSet.next()) {
                    Object[] item = new Object[3];
                    item[0] = resultSet.getString("thanhphan");
                    item[1] = resultSet.getString("heso");
                    item[2] = resultSet.getString("tieuchi");
                    tblModelTienIn.addRow(item);
                }
                ct.tblCongThucThanhPhan.setModel(tblModelTienIn);
                tblCongThucThanhPhanTienIn = ct.tblCongThucThanhPhan;
                ct.dispose();
            }
            ResultSet rsCongThucTienTho = MayInDAL.layCongThucIn(maMayIn, false);
            if (rsCongThucTienTho.next()) {
                congThucTienTho = new CongThuc(rsCongThucTienTho.getInt("ma_ct"), rsCongThucTienTho.getString("ten_ct"), rsCongThucTienTho.getString("loai"), rsCongThucTienTho.getString("ma_mayin"));
                //Công thức đã lưu
                ResultSet resultSet = MayInDAL.layCongThuc(rsCongThucTienTho.getInt("ma_ct"));
                frmCongThuc ct2 = new frmCongThuc();
                DefaultTableModel tblModelTienTho = (DefaultTableModel) ct2.tblCongThucThanhPhan.getModel();
                while (resultSet.next()) {
                    Object[] item = new Object[3];
                    item[0] = resultSet.getString("thanhphan");
                    item[1] = resultSet.getString("heso");
                    item[2] = resultSet.getString("tieuchi");
                    tblModelTienTho.addRow(item);
                }
                ct2.tblCongThucThanhPhan.setModel(tblModelTienTho);
                tblCongThucThanhPhanTienTho = ct2.tblCongThucThanhPhan;
                ct2.dispose();

            }
        } catch (SQLException ex) {
            Logger.getLogger(frmKeHoach.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblMayInMouseClicked

