    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //Đổ dữ liệu vào bảng Khách Hàng
        ResultSet rsKhachHang = KhachHangBLL.LayTatCaKhachHang();
        KhachHangBLL.DoDuLieuvaoJtaBle(rsKhachHang, tblKhachHang);

        //Đổ dữ liệu vào bản Giấy In
        ResultSet rsGiay = GiayInBLL.LayTatCaGiay();
        GiayInBLL.DoDuLieuvaoJtaBle(rsGiay, tblGiayIn);

        //Đổ dữ liệu vào bản DonHangBLL
        ResultSet rsDH = DonHangBLL.LayTatCaDonHang();
        DonHangBLL.DoDuLieuvaoJtaBle(rsDH, tblDonHang);

        //Đổ dữ liệu vào bảng Máy In
        ResultSet rsMayIn = MayInBLL.LayTatCaMayIn();
        MayInBLL.DoDuLieuvaoJtaBle(rsMayIn, tblMayIn);

        //Đổ dữ liệu vào bản Gia Công N
        ResultSet rsGiaCongN = GiaCongNBLL.LayTatCaGiaCongN();
        GiaCongNBLL.DoDuLieuvaoJtaBle(rsGiaCongN, tblGiaCongN);

        // Đổ dữ liệu vào combobox
        ResultSet rsKH = KhachHangBLL.LayTatCaKhachHang();
        try {
            while (rsKH.next()) {
                String maKH;
                maKH = rsKH.getString("ma_kh");
                cbxMaKhachHang.addItem(maKH);

            }
        } catch (SQLException ex) {
            Logger.getLogger(frmLenhIn.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

